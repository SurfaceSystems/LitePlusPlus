int calc(int calc) {
    return calc;
}

int sum(int num1, int num2) {
    return num1 + num2;
}

int sub(int num1, int num2) {
    return num1 - num2;
}

int mul(int num1, int num2) {
    return num1 * num2;
}

int rep(int num1, int num2) {
    return num1 / num2;
}

int dou(int num1) {
    return num1 * 2;
}

